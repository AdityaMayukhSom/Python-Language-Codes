# type casting = convertt the data type of a value to another data type

x = 1  # Integer
y = 2.0  # Float
z = "3"  # String

print(x)
print(y)
print(z)
print()

print(type(x))
print(type(y))
print(type(z))
print()

print(str(x))
print(int(y))
print(float(z))
print()

print(type(float(x)))
print(type(str(y)))
print(type(int(z)))
print()

print(type(x))
print(type(y))
print(type(z))
print()

# Without typecasting, interpreter would not be able to concatenate them
print("The value of x is " + str(x))
print("The value of y is " + str(y))
